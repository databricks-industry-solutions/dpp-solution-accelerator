def load_json(table_name: str) -> list[dict[str, Any]]:
    """Load a JSON file for the given table name."""
    path = DATA_DIR / f"{table_name}.json"
    if not path.exists():
        print(f"  SKIP {table_name} (no data file)")
        return []
    with open(path, "r", encoding="utf-8") as f:
        return json.load(f)


def prepare_value(value: Any, column: str) -> Any:
    """Convert Python values to what asyncpg sends correctly given the text
    codecs in _init_codecs: JSONB columns as JSON strings, lists as native
    arrays, everything else as-is."""
    if value is None:
        return None
    if column in ("old_values", "new_values"):
        return value if isinstance(value, str) else json.dumps(value)
    if isinstance(value, list):  # TEXT[] -> native asyncpg array
        return value
    if isinstance(value, dict):
        return json.dumps(value)
    return value


async def insert_table(
    conn: asyncpg.Connection,
    table_name: str,
    records: list[dict[str, Any]],
) -> int:
    """Insert records into a Lakebase table. Returns count inserted."""
    if not records:
        return 0

    columns = TABLE_COLUMNS[table_name]
    placeholders = ", ".join(["%s"] * len(columns))
    col_names = ", ".join(columns)
    sql = _to_pg(f"INSERT INTO dpp.{table_name} ({col_names}) VALUES ({placeholders})")

    rows = [
        tuple(prepare_value(record.get(col), col) for col in columns)
        for record in records
    ]
    await conn.executemany(sql, rows)
    return len(rows)


async def truncate_tables(conn: asyncpg.Connection) -> None:
    """Truncate all tables in reverse FK order.

    TRUNCATE does not fire the row-level AFTER INSERT/UPDATE/DELETE audit
    triggers, so no trigger suppression is needed. (We avoid
    `SET session_replication_role`, which is superuser-only and not available
    to the Lakebase app/job role.)
    """
    for table in TABLE_TRUNCATE_ORDER:
        await conn.execute(f"TRUNCATE TABLE dpp.{table} CASCADE;")
        print(f"  Truncated dpp.{table}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

async def _seed_async() -> None:
    """Seed all Lakebase tables from generated JSON data (file-based, local)."""
    apply_cli_overrides()
    print("=" * 60)
    print("DPP Lakebase Seeder")
    print("=" * 60)

    if not DATA_DIR.exists():
        print(f"ERROR: Data directory not found: {DATA_DIR}")
        print("Run 02_synthetic_data_generator.py first.")
        sys.exit(1)

    conn = await _connect()
    try:
        async with conn.transaction():
            print("\nTruncating existing data...")
            await truncate_tables(conn)
            print("\nLoading data...")
            total = 0
            for table_name in TABLE_LOAD_ORDER:
                records = load_json(table_name)
                if records:
                    count = await insert_table(conn, table_name, records)
                    print(f"  Inserted {count:>5} rows into dpp.{table_name}")
                    total += count
        print(f"\nTotal rows inserted: {total}")
        print("Done.")
    finally:
        await conn.close()


def main() -> None:
    run_async(_seed_async())


if __name__ == "__main__":
    main()
