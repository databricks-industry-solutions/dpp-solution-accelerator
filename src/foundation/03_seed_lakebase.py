def run_async(coro):
    """Run a coroutine to completion even when an event loop is already running.

    Databricks serverless spark_python_task executes inside an active asyncio
    loop, so asyncio.run() raises "cannot be called from a running event loop".
    Running on a fresh loop in a dedicated thread works in both cases.
    """
    box: dict = {}

    def _runner() -> None:
        loop = asyncio.new_event_loop()
        asyncio.set_event_loop(loop)
        try:
            box["result"] = loop.run_until_complete(coro)
        except BaseException as exc:  # noqa: BLE001
            box["error"] = exc
        finally:
            loop.close()

    thread = threading.Thread(target=_runner)
    thread.start()
    thread.join()
    if "error" in box:
        raise box["error"]
    return box.get("result")

