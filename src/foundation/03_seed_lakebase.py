"""
Digital Product Passport -- Lakebase Seeder

Reads JSON files generated by 02_synthetic_data_generator.py and inserts
them into the Lakebase PostgreSQL database. Idempotent: truncates each
table before inserting (respecting FK order).

Usage:
    python 03_seed_lakebase.py

Environment variables:
    LAKEBASE_HOST       Lakebase PostgreSQL hostname
    LAKEBASE_PORT       Port (default: 5432)
    LAKEBASE_DATABASE   Database name (default: dpp)
    LAKEBASE_USER       Database user
    LAKEBASE_PASSWORD   Database password

Alternatively, set LAKEBASE_CONNECTION_STRING for a full DSN.
"""

