def apply_cli_overrides(argv: list[str] | None = None) -> None:
    """Let the bundle pass --instance / --database as task parameters.

    Used by both this seeder and 00_apply_schema so a customer who renames the
    Lakebase instance only edits the bundle variables — no code change.
    """
    import argparse

    ap = argparse.ArgumentParser(add_help=False)
    ap.add_argument("--instance")
    ap.add_argument("--database")
    args, _ = ap.parse_known_args(argv)
    if args.instance:
        os.environ["LAKEBASE_INSTANCE"] = args.instance
    if args.database:
        os.environ["LAKEBASE_DATABASE"] = args.database


def _resolve_runtime_lakebase() -> tuple[str, str, str, str]:
    """Resolve (dns, database, user, oauth_token) using the Databricks runtime
    identity. Used inside jobs/notebooks so no static credentials are needed.

    Uses the REST API (not the SDK database client) for compatibility with
    older DBR images — matches the bronze ingestion pipeline.
    """
    import uuid

    import requests
    from databricks.sdk import WorkspaceClient

    instance = os.environ.get("LAKEBASE_INSTANCE", "dpp-passport")
    database = os.environ.get("LAKEBASE_DATABASE", "databricks_postgres")

    w = WorkspaceClient()
    host = w.config.host.rstrip("/")
    headers = w.config.authenticate()  # {"Authorization": "Bearer <token>"}

    inst = requests.get(
        f"{host}/api/2.0/database/instances/{instance}",
        headers=headers, timeout=30,
    )
    inst.raise_for_status()
    dns = inst.json()["read_write_dns"]

    cred = requests.post(
        f"{host}/api/2.0/database/credentials",
        headers=headers, timeout=30,
        json={"request_id": str(uuid.uuid4()), "instance_names": [instance]},
    )
    cred.raise_for_status()
    token = cred.json()["token"]

    me = requests.get(
        f"{host}/api/2.0/preview/scim/v2/Me", headers=headers, timeout=30,
    )
    me.raise_for_status()
    user = me.json()["userName"]

    return dns, database, user, token


_PLACEHOLDER_RE = re.compile(r"%s")


def _to_pg(query: str) -> str:
    """Convert psycopg-style %s placeholders to asyncpg's positional $1, $2, …"""
    counter = {"n": 0}

    def repl(_match: re.Match) -> str:
        counter["n"] += 1
        return f"${counter['n']}"

    return _PLACEHOLDER_RE.sub(repl, query)


# The synthetic data carries dates/timestamps as ISO strings, UUIDs as strings,
# and numerics as floats. Register text-format codecs so asyncpg sends them with
# the correct PG type but in text form (the server parses) — this mirrors
# psycopg's lenient text protocol so the generator's output flows through as-is.
_TEXT_CODEC_TYPES = ("uuid", "date", "timestamp", "timestamptz", "json", "jsonb")


async def _init_codecs(conn: asyncpg.Connection) -> None:
    for type_name in _TEXT_CODEC_TYPES:
        await conn.set_type_codec(
            type_name, schema="pg_catalog",
            encoder=lambda v: v, decoder=lambda v: v, format="text",
        )
    await conn.set_type_codec(
        "numeric", schema="pg_catalog",
        encoder=lambda v: str(v), decoder=lambda v: v, format="text",
    )


async def _connect() -> asyncpg.Connection:
    """Connect to Lakebase (asyncpg). Two paths:

      1. LAKEBASE_HOST + LAKEBASE_PASSWORD — explicit native login (local).
      2. Databricks runtime — self-authenticate with a short-lived OAuth token
         from the job's identity (no secrets). This is what the bundle's setup
         job uses, so customer redeploys need zero credential wiring.
    """
    if os.environ.get("LAKEBASE_HOST"):
        conn = await asyncpg.connect(
            host=os.environ["LAKEBASE_HOST"],
            port=int(os.environ.get("LAKEBASE_PORT", "5432")),
            database=os.environ.get("LAKEBASE_DATABASE", "databricks_postgres"),
            user=os.environ["LAKEBASE_USER"],
            password=os.environ["LAKEBASE_PASSWORD"],
            ssl=True,
        )
    else:
        dns, database, user, token = _resolve_runtime_lakebase()
        conn = await asyncpg.connect(
            host=dns, port=5432, database=database, user=user,
            password=token, ssl=True,
        )
    await _init_codecs(conn)
    return conn


