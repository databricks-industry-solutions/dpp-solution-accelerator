def _script_dir() -> Path:
    """Directory containing this script.

    Databricks serverless ``spark_python_task`` does NOT define ``__file__``,
    so fall back to argv[0]. Used to locate the generated data dir.
    """
    try:
        return Path(__file__).resolve().parent
    except NameError:
        return Path(sys.argv[0]).resolve().parent


